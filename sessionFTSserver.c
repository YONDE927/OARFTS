//規則
//サーバーは基本受け身である。
//クライアント側のリクエストに応じる。(常にrecv)
//recvの結果によってシンプルな結果を返すものや、ファイル送信のような複雑なセッションになる場合がある。
//そういったリクエストを文字列と構造体のどちらかで送信する必要がある。
//HTTPを参考にするとGETやPOSTのように文字列でサーバーに指示を与えることができる。
//構造体にする場合は、バイトオーダーに対応すれば拡張性の大きなデータ通信ができる。
//
//OARFSで用いるような通信モジュールを実現するにはサーバーは状態を持つ必要がある。
//理由は、openの命令を発したときにサーバー側でファイルディスクリプタを保持しておかないと,
//後のread通信で毎回ファイルをオープンしなければならないからだ。
//よって、以下のような状態をもつ。
//・openしているファイルディスクリプタのリスト
//・クライアントの情報
//・現在のセッションのモード
//
//受信->セッション処理->待機状態の流れだと,あらたなリクエストを受け付けることができるのは待機状態のときのみである。
//
//クライアントごとにソケットを作成し、この一連の処理をスレッドとして分離する。
//そのためにはサーバーの状態は最大のクライアント数に応じて持つ必要がある。(サーバー構造体の配列)
//
